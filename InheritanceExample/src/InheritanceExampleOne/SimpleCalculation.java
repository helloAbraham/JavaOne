package InheritanceExampleOne;

public class SimpleCalculation {
    int sum;

    public void addition(int firstNum, int secondNum){
        sum = firstNum + secondNum;
        System.out.println("The sum of the given input is " + sum);
    }

    public void subtraction(int fNumber, int sNumber){
        sum = fNumber - sNumber;
        System.out.println("The differences two inputs is " + sum);
    }

}
